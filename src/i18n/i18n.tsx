const dictionaries: Record<Language, Record<TranslationKey, string>> = { zh, en };

interface I18nContextValue {
  language: Language;
  languagePreference: LanguagePreference;
  setLanguage: (language: LanguagePreference) => Promise<void>;
  setLanguagePreference: (language: LanguagePreference) => Promise<void>;
  t: (key: TranslationKey, values?: TranslationValues) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: PropsWithChildren) {
  const [languagePreference, setLanguagePreferenceState] = useState<LanguagePreference>('system');
  const [systemLanguage] = useState<Language>(detectSystemLanguage);

  useEffect(() => {
    void getStoredLanguagePreference().then(setLanguagePreferenceState);
  }, []);

  const value = useMemo<I18nContextValue>(() => {
    const language = resolveLanguage(languagePreference, systemLanguage);
    const setLanguagePreference = async (nextLanguage: LanguagePreference) => {
      setLanguagePreferenceState(nextLanguage);
      await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
    };

    return {
      language,
      languagePreference,
      setLanguage: setLanguagePreference,
      setLanguagePreference,
      t: (key, values) => interpolate(dictionaries[language][key] ?? dictionaries.zh[key] ?? key, values),
    };
  }, [languagePreference, systemLanguage]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error('useI18n must be used inside I18nProvider.');
  }
  return value;
}

async function getStoredLanguagePreference(): Promise<LanguagePreference> {
  const raw = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY);
  return raw === 'en' || raw === 'zh' || raw === 'system' ? raw : 'system';
}

function resolveLanguage(languagePreference: LanguagePreference, systemLanguage: Language): Language {
  return languagePreference === 'system' ? systemLanguage : languagePreference;
}

function detectSystemLanguage(): Language {
  const locale = getSystemLocale().trim().toLowerCase();
  return locale.startsWith('zh') || locale.includes('-zh') || locale.includes('_zh') ? 'zh' : 'en';
}

function getSystemLocale() {
  const intlLocale = readIntlLocale();
  if (intlLocale) {
    return intlLocale;
  }

  const settingsLocale = readSettingsManagerLocale();
  if (settingsLocale) {
    return settingsLocale;
  }

  const i18nLocale = readI18nManagerLocale();
  return i18nLocale || 'en';
}

function readIntlLocale() {
  try {
    return Intl.DateTimeFormat().resolvedOptions().locale || '';
  } catch {
    return '';
  }
}

function readSettingsManagerLocale() {
  const settingsManager = NativeModules.SettingsManager as
    | {
        settings?: { AppleLocale?: string; AppleLanguages?: string[] };
        getConstants?: () => { settings?: { AppleLocale?: string; AppleLanguages?: string[] }; AppleLocale?: string; AppleLanguages?: string[] };
      }
    | undefined;
  const constants = settingsManager?.getConstants?.();
  const settings = settingsManager?.settings ?? constants?.settings ?? constants;
  const appleLocale = settings?.AppleLocale;
  if (typeof appleLocale === 'string' && appleLocale.trim()) {
    return appleLocale;
  }

  const appleLanguage = settings?.AppleLanguages?.[0];
  return typeof appleLanguage === 'string' ? appleLanguage : '';
}

function readI18nManagerLocale() {
  const i18nManager = NativeModules.I18nManager as
    | {
        localeIdentifier?: string;
        getConstants?: () => { localeIdentifier?: string };
      }
    | undefined;
  const locale = i18nManager?.getConstants?.()?.localeIdentifier ?? i18nManager?.localeIdentifier;
  return typeof locale === 'string' ? locale : '';
}

function interpolate(template: string, values?: TranslationValues) {
  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (match, key) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}
